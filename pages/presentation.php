	<h3>LA BELLE EQUIPE</h3>
	<p>Après quelques années passées dans le conseil, l’ingénierie durable et l’audit, Claire et Delphine ont décidé de se consacrer à la transition écologique et humaine en promouvant les objets responsables et l'histoire des fabricants à l’origine de ces objets.</p> 
	<p>Ce projet rassemble les idées et les coups de mains de toute une belle équipe (bénévoles, étudiants, amis) !  Un grand merci ! Vous pouvez aussi nous rejoindre facilement. Pour participez à nos apéros créatifs ou donner des idées et pistes d’amélioration pour le projet, envoyez nous un message via les réseaux sociaux.  </p>


	<h3>LA METHODE </h3>

	<p>Notre approche se veut complète sur l’ensemble des critères : écologie, santé, qualité, emplois etc en prenant en compte l’ensemble des étapes de production et du cycle de vie de l’objet y compris la fin d’usage. 
	Sinon une analyse court le risque de se concentrer sur l’arbre qui cache la forêt. Par exemple on peut fabriquer un T-Shirt en coton biologique peut être cousu au Bangladesh dans de mauvaises conditions et un objet made in France peut utiliser des matières lointaines. </p>
	<p>Pour chaque catégorie d’objets, nous identifions des défis clefs qui sont les points les plus à risque que nous étudions en priorité et plus en détails, par exemple le tannage pour le cuir ou la gestion de l’eau pour le textile. Cette analyse s’appuie sur des nombreux conseils de spécialistes et d’experts. </p>
	<p>Nous réalisons des audits et prenons en compte tous les audits sérieux déjà réalisés par les autres labels et normes à chaque étape de production (normes ISO, labels Oekotex etc. ).</p>
	<p>Tous les objets labellisés ont un fort impact positif à travers une bonne maîtrise de leur production. Nous distinguons néanmoins 3 niveaux dans le label afin de mettre en avant les acteurs qui apportent en plus une dimension d’innovation inspirante à leur secteur. </p>

	<h3>LES OUTILS  </h3>
	<p>La Belle Empreinte vous propose deux outils pour simplifier au quotidien une consommation responsable  :</p>

	<p>Le label des objets responsables<br/>
	-Un label complet : tous les critères clefs inclus (écologie, emploi, santé, qualité)<br/>
	-Un label fiable : un audit selon une méthode rigoureuse<br/>
	-Un label clair : une étiquette par objet qui explique la labellisation et la fabrication</p>
	<p>Le moteur de recherche des objets responsables <br/>
	-Un outil utile : les conseils fiables et les bonnes adresses<br/>
	-Un outil pratique : les informations centralisées et classées par objet et thème (neuf, occasion, réparer, fin d’usage)<br/>
	</p>
	<p>Ces nouveaux outils se lancent : merci de votre patience :). Les toutes premières marques labellisées sont présentées sur notre site, suivez-nous pour découvrir les autres au fur et à mesure.</p>


